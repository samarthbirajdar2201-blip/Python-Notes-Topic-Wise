# ------------------------------
# Python Data Types
# ------------------------------

# 1. Integer (int)
age = 20
print("Integer:", age)
print(type(age))
print()

# 2. Float (float)
height = 5.8
print("Float:", height)
print(type(height))
print()

# 3. String (str)
name = "Sam"
print("String:", name)
print(type(name))
print()

# 4. Boolean (bool)
is_student = True
print("Boolean:", is_student)
print(type(is_student))
print()

# 5. Complex (complex)
num = 3 + 4j
print("Complex:", num)
print(type(num))
print()

# 6. List (list)
fruits = ["Apple", "Mango", "Banana"]
print("List:", fruits)
print(type(fruits))
print()

# 7. Tuple (tuple)
colors = ("Red", "Green", "Blue")
print("Tuple:", colors)
print(type(colors))
print()

# 8. Set (set)
numbers = {10, 20, 30, 40}
print("Set:", numbers)
print(type(numbers))
print()

# 9. Dictionary (dict)
student = {
    "Name": "Sam",
    "Age": 20,
    "City": "Pune"
}
print("Dictionary:", student)
print(type(student))
print()

# 10. NoneType
value = None
print("None:", value)
print(type(value))