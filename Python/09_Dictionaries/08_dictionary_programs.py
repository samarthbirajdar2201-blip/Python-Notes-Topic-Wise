student = {
    "Name": "Sam",
    "Age": 21,
    "City": "Pune"
}

# Search Key

if "Age" in student:
    print("Key Found")
else:
    print("Key Not Found")

print()

# Print all Keys

print(student.keys())

print()

# Print all Values

print(student.values())