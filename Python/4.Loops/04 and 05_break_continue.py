# 4. Break Statement
# ------------------------------

# Stop the loop when number becomes 5

for i in range(1, 11):

    if i == 5:
        break

    print(i)
    
    
# ------------------------------
# 5. Continue Statement
# ------------------------------

# Skip number 5

for i in range(1, 11):

    if i == 5:
        continue

    print(i)